public class Primitive_data_type {
    public static void main(String[] args) {
        int a = 2000;
        short b = 30;
        long c = 400000000000000000L;
        float d = 5.6f;
        double e = 512.55D;
        char f = 'K';
        byte g = 100;
        boolean h = true;
        System.out.println("The value of integer " +a);
        System.out.println("The value of short " +b);
        System.out.println("The value of long " +c);
        System.out.println("The value of float " +d);
        System.out.println("The value of double " +e);
        System.out.println("The value of char " +f);
        System.out.println("The value of byte " +g);
        System.out.println("The value of boolean " +h);
    }
}
